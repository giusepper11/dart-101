main() {
  // Math
  int a = 7;
  int b = 3;

  int result = a + b;
  print(result);

  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  // boleans
  bool fragile = true;
  bool expensive = false;

  print(fragile && expensive); //and
  print(fragile || expensive); //or
  print(fragile ^ expensive); //or exclusive (XOR)
  print(!fragile); //not
}
